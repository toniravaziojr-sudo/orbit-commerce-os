function getIconSvg(iconName: string): string {
  const key = iconName.toLowerCase().replace(/_/g, '');
  return ICON_SVG[key] || ICON_SVG.check;
}

interface FeatureItem {
  id?: string;
  icon: string;
  text: string;
}

export const featureListToStaticHTML: BlockCompilerFn = (
  props: Record<string, unknown>,
  _context: CompilerContext,
): string => {
  const title = (props.title as string) || '';
  const subtitle = (props.subtitle as string) || '';
  const items = (props.items as FeatureItem[]) || [];
  const iconColor = (props.iconColor as string) || '';
  const textColor = (props.textColor as string) || '';
  const showButton = !!props.showButton;
  const buttonText = (props.buttonText as string) || 'Saiba mais';
  const buttonUrl = (props.buttonUrl as string) || '#';
  const backgroundColor = (props.backgroundColor as string) || 'transparent';

  if (items.length === 0) return '';

  const effectiveIconColor = iconColor || 'var(--theme-accent-color, #22c55e)';
  const textStyle = textColor ? `color:${textColor};` : '';

  const itemsHtml = items.map(item => `
    <li style="display:flex;align-items:flex-start;gap:0.75rem;">
      <span style="flex-shrink:0;margin-top:0.125rem;color:${effectiveIconColor};">${getIconSvg(item.icon)}</span>
      <span style="font-size:1rem;${textStyle}">${escapeHtml(item.text)}</span>
    </li>`).join('');

  const ctaHtml = showButton && buttonText ? `
    <div style="margin-top:1.5rem;">
      <a href="${escapeHtml(buttonUrl)}" class="sf-btn-primary" style="display:inline-flex;align-items:center;justify-content:center;padding:0.75rem 1.5rem;border-radius:0.5rem;font-weight:600;text-decoration:none;">${escapeHtml(buttonText)}</a>
    </div>` : '';

  return `
    <section style="background-color:${backgroundColor};padding:2rem 1rem;">
      <div style="max-width:48rem;margin:0 auto;">
        ${title ? `<h2 style="font-size:1.5rem;font-weight:700;margin-bottom:0.5rem;${textStyle}">${escapeHtml(title)}</h2>` : ''}
        ${subtitle ? `<p style="color:var(--theme-text-secondary, #666);margin-bottom:1.5rem;">${escapeHtml(subtitle)}</p>` : ''}
        <ul style="list-style:none;padding:0;margin:0;display:flex;flex-direction:column;gap:0.75rem;">
          ${itemsHtml}
        </ul>
        ${ctaHtml}
      </div>
    </section>`;
};
